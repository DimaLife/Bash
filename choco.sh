#! /bin/bash

echo "Введите число долек n"
read n
echo "Введите число долек m"
read m
echo "Введите количество долек"
read k
s=$(( $n*$m ))
	echo "$s"
if [[ $k -lt $s ]] && [[ $k%$n -eq 0 || $k%$m -eq 0 ]]
then
	echo "Можно отломить $k долек от шоколадки размером $s долек"
else
	echo "Нельзя отломить $k долек от шоколадки размером $s долек"
fi
